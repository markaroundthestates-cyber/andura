/** Map for O(1) id → preset resolution (UserAvatar render path). */
const PRESET_BY_ID: ReadonlyMap<string, AvatarPreset> = new Map(
  AVATAR_PRESETS.map((p) => [p.id, p]),
);

/** Resolve a preset by id. Returns null for an unknown/null id (→ initials
 *  fallback in UserAvatar). Pure — safe in render. */
export function getAvatarPreset(id: string | null | undefined): AvatarPreset | null {
  if (!id) return null;
  return PRESET_BY_ID.get(id) ?? null;
}

/** i18n key for a preset's human-readable name (aria-label / picker caption). */
export function avatarPresetLabelKey(id: string): string {
  return `cont.avatar.presets.${id}`;
}
