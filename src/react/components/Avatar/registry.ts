/** A Pulse accent token used as the glyph stroke/fill. */
export type AvatarAccent =
  | 'var(--volt)'
  | 'var(--aqua)'
  | 'var(--ember)'
  | 'var(--violet)';

/**
 * A preset avatar. `kind` selects the render branch in UserAvatar:
 *   - 'svg'   → `glyph` draws an inline Pulse line-art path (current set).
 *   - 'image' → `src` points at a square raster (future founder AI set). The
 *               glyph/accent fields are ignored for image entries.
 * The id is the persisted contract — keep ids stable across asset swaps.
 */
export interface AvatarPreset {
  /** Stable persisted id (settingsStore.avatarId). Never reuse/rename. */
  id: string;
  kind: 'svg' | 'image';
  /** SVG entries: the Pulse accent token the glyph is drawn in. */
  accent?: AvatarAccent;
  /** SVG entries: renders the inner glyph at the given accent (viewBox 0 0 48 48). */
  glyph?: (accent: AvatarAccent) => JSX.Element;
  /** Image entries (future AI set): absolute/public URL to a square asset. */
  src?: string;
}

