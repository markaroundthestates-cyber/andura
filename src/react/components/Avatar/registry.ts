// ══ AVATAR REGISTRY — illustrated preset set (Pulse style) ════════════════
// The single source of truth for selectable account avatars. Each entry maps a
// stable `id` (persisted in settingsStore.avatarId + synced per-UID) to a
// renderable asset. Founder pick 2026-06-12 "Preset-uri ilustrate" — an
// illustrated preset set in the Pulse line-art style, no real faces, athletic +
// gender-neutral, friendly for both Gigel and Maria 65.
//
// SWAPPABLE ASSET LAYER (design directive): the renderer (UserAvatar) branches
// on `kind`. Today every entry is `kind: 'svg'` — an inline Pulse line-art glyph
// (zero asset bytes, theme-token colored, scales crisp at any px). A future
// founder-generated AI image set (square PNG/webp, emerald/teal accents) drops
// in with ZERO refactor: add `{ id, kind: 'image', src: '/avatars/<id>.webp' }`
// entries (or flip an existing id's kind+src). UserAvatar already renders the
// <img> branch. No consumer, store, or persistence change is needed — the id
// contract is stable, only the asset behind it changes.
//
// Colors: LIVE Pulse tokens only (--volt emerald / --aqua teal / --ember amber /
// --violet) — never raw hex, so avatars track theme + the accent picker palette.
// The glyph fill colors below are token var() strings resolved at render.
//
// A11y: the human-readable name lives in i18n under `cont.avatar.presets.<id>`
// (UserAvatar / AvatarPicker pass it as aria-label); this module stays
// string-free for locale independence.

import type { JSX } from 'react';
